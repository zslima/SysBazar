    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        int fila = tbVenta.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "Seleccionar LA VENTAo a ELIMINAR");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea ELIMINAR el Usuario? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int celda = (int) tbVenta.getValueAt(fila, 0);
                int x = ven.eliminarVenta(celda);
                if(x==1){
                    JOptionPane.showMessageDialog(null, "VENTA ELIMINADA!");
                    updateComponets();
                    limpiar();
                }else{
                    JOptionPane.showMessageDialog(null, "VENTA NO ELIMINADO!");
                }
            }

        }
    }//GEN-LAST:event_btnEliminar1ActionPerformed

    private void btnModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar1ActionPerformed
        int fil = tbVenta.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Venta a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR el venta? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int id = Integer.parseInt(txtidVenta.getText());
                int idc = Integer.parseInt(txtidCliente.getText());
                int ide = Integer.parseInt(txtidEmpleado.getText());
                String fec = jdFecha.getDateFormatString();
                 String hora = txtHora.getText();
                 int cod_doc = Integer.parseInt(txtCodDocument.getText());
                 String tipo_doc = txtTipoDocument.getText();
                int x = ven.modificarVenta(id,idc,ide, fec, hora,cod_doc,tipo_doc);

                if(x==1){
                    JOptionPane.showMessageDialog(null, "Venta MODIFICADO!");
                    updateComponets();
                    limpiar();

                }else{
                    JOptionPane.showMessageDialog(null, "Producto no se ha MODIFICADO!");
                }
            }
        }
    }//GEN-LAST:event_btnModificar1ActionPerformed

    private void btnagregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregar1ActionPerformed
        // TODO add your handling code here:
        if(cboCliente.getSelectedIndex()!=0||cboEmpleado.getSelectedIndex()!=0){
            int idc = Integer.parseInt(txtidCliente.getText());
            int ide = Integer.parseInt(txtidEmpleado.getText());
            String fecha = ((JTextField)jdFecha.getDateEditor().getUiComponent()).getText();
            String hora = txtHora.getText();
            int cod_doc =Integer.parseInt( txtCodDocument.getText());
            String tipo_doc=txtTipoDocument.getText();
            int v = ven.registrarVenta(idc,ide,fecha,hora,cod_doc,tipo_doc);
            
            limpiar();
            if(v >0){
                JOptionPane.showMessageDialog(this, "Exitoso");}
            updateComponets();

        }else{
            JOptionPane.showMessageDialog(this, "Seleccionar cliente");
        }
    }//GEN-LAST:event_btnagregar1ActionPerformed
