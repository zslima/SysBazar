final void listarVenta(){
    lista = ven.listarVenta();
    model = (DefaultTableModel) tbVenta.getModel();
        Object[] vent = new Object[7];
        for(int i=0;i<lista.size();i++){
            vent[0]=lista.get(i).getIdVenta();
            vent[1]=lista.get(i).getIdemp();
            vent[2]=lista.get(i).getIdcli();
            vent[3]=lista.get(i).getFecha();
            vent[4]=lista.get(i).getHora();
            vent[5]=lista.get(i).getCod_doc();
            vent[6]=lista.get(i).getTipo_dog();
            model.addRow(vent);
        }        
        tbVenta.setModel(model);
}
void LimpiarTabla(DefaultTableModel modelo){
        int a =modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){  
            modelo.removeRow(i);
        }
 }

